        private void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblPersons.getModel();
        dtm.setRowCount(0);
        for(User person:userDirectory.getUserList()){
            Object[] row = new Object[6];
            row[0] = person.getUsername();
            row[1] = person;
            row[2] = person.getLastName();
            row[3] = person.getNuid();
            String hashedPassword = BCrypt.hashpw(new String(person.getPassword()), BCrypt.gensalt());
            row[4] = hashedPassword;
            row[5] = "Yes";
            dtm.addRow(row);
        }
         dtm.removeRow(0);
    }    
